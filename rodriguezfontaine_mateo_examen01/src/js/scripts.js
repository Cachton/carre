document.getElementById("carte1").addEventListener("click", renverseCarte);

function renverserCarte1() {
    var x = document.getElementById("carte1");
    var y = document.getElementById("dos1")
    if (x.style.display === "none") {
        x.style.display = "block";
        y.style.display = "none";
    } else {
        x.style.display = "none";
        y.style.display = "block";
    }
  }

